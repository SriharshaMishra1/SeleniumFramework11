package Basicjava;

public class Switch_condition {
	public static void main(String[] args) {
		
		int rama=50;
		switch(rama)
		{
		case 20:System.out.println("right");
		case 49:System.out.println("wrong");
		case 50:System.out.println("equal");
		default:System.out.println("dont know");
		}
}
}

