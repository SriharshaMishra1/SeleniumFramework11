package Basicjava;

public class conversion {
	//Primitive conversion
	public static void main(String[] args) {
		double d=(double)15; //Widening-converting small data type to bigger data
	
	int i= (int)25.5;//narrowing-converting bigger data type to smaller data type
	
	System.out.println(" widering result is"+" "+d);
	System.out.println(" Narrowing result is"+" "+i);
	}
	
	
	

}
